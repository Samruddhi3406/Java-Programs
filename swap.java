class swap
{
public static void main(String[] args)
{
 int a=10;
 int b=5;
 System.out.println("Values before swapping:");
 System.out.println("a="+a );
 System.out.println("b=" +b);
 int z;
 z=a;
 a=b;
 b=z;
 System.out.println("values after swapping: ");
 System.out.println("a=" +a);
 System.out.println("b=" +b);
}
}